    # I calcoli tradizionali per generare i-ching dal lancio di una moneta 
    # assegnano a "testa" il valore di 3 e a "croce" il valore di due.
    def cointoss(self):
        rand_i = random.randint(0, 1)
        outcomes = [3, 2]
        
        return outcomes[rand_i]

    # Each line's value is the sum of the values generated by the toss of three coins
    def generate_line(self):
        input("Premi Invio per lanciare le monete.")
        return self.cointoss() + self.cointoss() + self.cointoss()
    
    def locate_hexagram(hex):
        int_hex = int(hex)

        # Opening JSON file
        f = open('hexagrams.json')
        
        # returns JSON object as a dictionary
        data = json.load(f)

        found = {}

        for d in data['hexagrams']:
            if d['pattern'] == int_hex:
                found = d

        # Closing file
        f.close()

        return found

    def log_format(type, transforming_line):
        result = Line.locate_hexagram(type)
        print("   ", result['name']['zh'], "   ")
        print(color.BOLD,"Esagramma",result['number'],": ",result['name']['it'],color.END)
        print("   ", result['symbol'], "   ")
        print(color.UNDERLINE + "La sentenza:" + color.END)
        print(result['judgment'])
        print(color.UNDERLINE + "Immagine:" + color.END)
        print(result['image'])
        if transforming_line:
            print(color.UNDERLINE + "Linee Mobili:" + color.END)
            for x in transforming_line:
                print(result['transforming_lines'][x])
        print ("\n")

    # Le linee con valore 6 o 9 sono considerate "mutevoli" e di conseguenza
    # viene generato un secondo esagramma.
    def convert_hexagrams(hexagram):
        changing = False
        primary = ""
        relating = ""
        transforming_lines = []
        for i in range(len(hexagram)):
            if hexagram[i] == "6":
                changing = True
                primary += str(8)
                relating += str(7)
                transforming_lines.append(i)
            elif hexagram[i] == "9":
                changing = True
                primary += str(7)
                relating += str(8)
                transforming_lines.append(i)
            else:
                primary += hexagram[i]
                relating += hexagram[i]
        if len(transforming_lines)==6:
            transforming_lines=[7]
        if changing:
            Line.log_format(primary, transforming_lines)
            Line.log_format(relating, transforming_lines)
        else:
            Line.log_format(primary, transforming_lines)


    # Ogni esagramma è composto da sei linee
    def generate_hexagram(self):
        hexagram = ""
        for x in range(6):
            hexagram += str(self.generate_line())
        Line.convert_hexagrams(hexagram)



